'use client'

import Link from 'next/link'
import { usePathname, useRouter } from 'next/navigation'
import { ApexSession } from '@/lib/session'

const navItems = [
  { href: '/dashboard', label: 'Overview', icon: HomeIcon },
  { href: '/conversations', label: 'Conversations', icon: ChatIcon },
  { href: '/ai-config', label: 'AI Config', icon: SparklesIcon },
  { href: '/business', label: 'Business', icon: BuildingIcon },
  { href: '/connect', label: 'Connections', icon: LinkIcon },
  { href: '/referrals', label: 'Referrals', icon: GiftIcon },
]

export default function Sidebar({ businessName }: { businessName: string }) {
  const pathname = usePathname()
  const router = useRouter()

  const handleLogout = () => {
    ApexSession.clear()
    router.push('/login')
  }

  return (
    <aside className="fixed left-0 top-0 h-screen w-64 bg-white border-r border-slate-200 flex flex-col">
      {/* Logo */}
      <div className="p-5 border-b border-slate-200">
        <Link href="/dashboard" className="flex items-center gap-2">
          <span className="text-2xl">⚡</span>
          <div className="font-bold text-xl text-slate-900">
            Apex<span className="text-orange-500">Automation</span>
          </div>
        </Link>
      </div>

      {/* Business Name */}
      <div className="px-5 py-3 border-b border-slate-200 bg-slate-50">
        <div className="text-[10px] text-slate-400 uppercase tracking-wider mb-0.5">Business</div>
        <div className="font-medium text-sm truncate text-slate-900">{businessName}</div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-3 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href || 
            (item.href !== '/dashboard' && pathname?.startsWith(item.href))
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl transition-all duration-200 ${
                isActive 
                  ? 'bg-orange-50 text-orange-600' 
                  : 'text-slate-600 hover:bg-slate-100 hover:text-slate-900'
              }`}
            >
              <item.icon className="w-5 h-5" />
              <span className="font-medium text-sm">{item.label}</span>
            </Link>
          )
        })}
      </nav>

      {/* Settings & Logout */}
      <div className="p-3 border-t border-slate-200">
        <Link
          href="/settings"
          className={`flex items-center gap-3 px-3 py-2.5 rounded-xl transition-all duration-200 mb-1 ${
            pathname === '/settings'
              ? 'bg-orange-50 text-orange-600'
              : 'text-slate-600 hover:bg-slate-100 hover:text-slate-900'
          }`}
        >
          <SettingsIcon className="w-5 h-5" />
          <span className="font-medium text-sm">Settings</span>
        </Link>
        <button
          onClick={handleLogout}
          className="flex items-center gap-3 px-3 py-2.5 rounded-xl text-slate-500 hover:text-red-500 hover:bg-red-50 w-full transition-all duration-200"
        >
          <LogoutIcon className="w-5 h-5" />
          <span className="font-medium text-sm">Sign out</span>
        </button>
      </div>
    </aside>
  )
}

